package main.java.com.collections;

public class Temple {
    public String name;
    public String religion;
    public Integer priests;
    public Boolean needsCandles;

    public Temple(String name, String religion, Integer priests, Boolean needsCandles) {
        this.name = name;
        this.religion = religion;
        this.priests = priests;
        this.needsCandles = needsCandles;
    }


}
